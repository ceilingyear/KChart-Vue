export function getDistance(touch1: any, touch2: any) {
  const xDiff = touch1.clientX - touch2.clientX;
  const yDiff = touch1.clientY - touch2.clientY;
  return Math.sqrt(xDiff * xDiff + yDiff * yDiff);
}
