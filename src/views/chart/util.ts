export function setupCanvas(canvas: HTMLCanvasElement) {
  const ctx = canvas.getContext("2d");
  if (!ctx) {
    console.error('canvas.getContext("2d") is null');
    return;
  }
  if (!isMobileDevice()) return ctx
  const htmlCanvasRect: any = canvas.getBoundingClientRect();

  canvas.width = htmlCanvasRect.width * dpr;
  canvas.height = htmlCanvasRect.height * dpr;
  canvas.style.width = htmlCanvasRect.width + "px";
  canvas.style.height = htmlCanvasRect.height + "px";

  ctx.scale(dpr, dpr);
  return ctx;
}
